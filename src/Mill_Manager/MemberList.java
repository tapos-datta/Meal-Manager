     void File_Load(){
           
        try {
            FileInputStream fin=new FileInputStream("initialization.ser");
            ObjectInputStream out=new  ObjectInputStream(fin);
            
            try {
                 result=(Initialization) out.readObject();
                 out.close();
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(MillDataUpdate.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        } catch (FileNotFoundException ex) {
           
            JOptionPane.showMessageDialog(null, "Initialize data first\n Thank you");
            check=1;
            MainWindow.mainWindow.setVisible(true);
            
        } catch (IOException ex) {
            Logger.getLogger(MillDataUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }
     }
     /**
      * in edittxtarea method set font of textarea 
      * editable of textarea is false;
      */
     void edittxtarea(){
          Font font=txtarea.getFont();
         float size = font.getSize() + 10.0f;
           txtarea.setFont( font.deriveFont(size) );
          
          txtarea.setEditable(false);
          
     }
    
    
     /**
      * write the member name in textarea by Write_name method 
      * 
      */
    void write_name(){
        
        txtarea.setText("");
        for(int i=0;i<result.member;i++)
        {
            String s="";
            s=(i+1)+". "+result.nameList[i];
            txtarea.append( s+ "\n");
            
        }
        
    }

