    /**
     * 
     * @param evt updatename button update the name of member 
     */
    private void updatenameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatenameActionPerformed
        try{
            int u= Integer.parseInt(JOptionPane.showInputDialog(null,"Enter the serial number of member"));
            if(u<1 || u>result.member) 
                throw new NullPointerException();
                
            String n=JOptionPane.showInputDialog("Update Name Of Member");
            
                if(n.isEmpty())
                throw new NullPointerException();
            
            result.nameList[u-1]=n;
            write_name();
            result.firstcolset();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Please, Enter valid information");
        }
        
        file_write();
    }//GEN-LAST:event_updatenameActionPerformed
    
    /**
     * updated data is save in initialization.ser file  by file_write method  
     */
    void file_write(){
           try {
               FileOutputStream fos= new  FileOutputStream("initialization.ser");
               
               try {
                   ObjectOutputStream out=new ObjectOutputStream(fos);
                   
                   out.writeObject(result);
                   out.close();
               } catch (IOException ex) {
                   Logger.getLogger(Initial.class.getName()).log(Level.SEVERE, null, ex);
               }
           } catch (FileNotFoundException ex) {
               Logger.getLogger(Initial.class.getName()).log(Level.SEVERE, null, ex);
           }
       }
    
